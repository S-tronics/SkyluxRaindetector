/**
 * @brief   Initialise function for this module
 *

 */
void StdCRCInit(void);

/**
 * @brief   Function to calculate the CRC-value of an amount of data
 *
 *	This method is used tot calculate the CRC-value, the crc is fully calculated
 *	with CRC_POLY-value
 *
 *  @param	crc				: crc as parameter to start crc-calculation
 *	@param	data			: data to be used for crc-calculation
 *	@param  n_bytes			: number of bytes, length of data array
 *
 *	@retrun					: Calculated crc-value
 */
uint32_t StdCRC32Calc(uint32_t crc, const uint8_t *data, uint16_t n_bytes);

/**
 * @brief   Function to calculate the CRC-value of an amount of data
 *
 *	This method is used tot calculate the CRC-value, the crc-value is calcuated
 *  with pre-stored values in table 'default_crc32_table'
 *
 *  @param	crc				: crc as parameter to start crc-calculation
 *	@param	data			: data to be used for crc-calculation
 *	@param  n_bytes			: number of bytes, length of data array
 *
 *	@retrun					: Calculated crc-value
 */
uint32_t StdCRC32Table(uint32_t crc, const uint8_t *data, uint16_t n_bytes);

