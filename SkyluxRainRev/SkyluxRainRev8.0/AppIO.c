void AppIoInit(void)
{
    //Init Relais Output
	gpio_set_pin_direction(PA23, GPIO_DIRECTION_OUT);
	gpio_set_pin_level(PA23, false);
    //Init Output
	gpio_set_pin_direction(PA22, GPIO_DIRECTION_OUT);
	gpio_set_pin_level(PA22, false);
}
/*--------------------------------------------------------------------------------------------------------------------*/
void AppIORelais(uint8_t onoff)
{
	switch(onoff)
	{
		case 1:
			gpio_set_pin_level(PA23, false);
			break;
		case 0:
			gpio_set_pin_level(PA23, true);
			break;
		default:
			break;
	}
}
/*--------------------------------------------------------------------------------------------------------------------*/
void AppIODetRain(uint8_t onoff)
{
	switch(onoff)
	{
		case 1:
			gpio_set_pin_level(PA22, true);
			break;
		case 0:
			gpio_set_pin_level(PA22, false);
			break;
		default:
			break;
	}
}
